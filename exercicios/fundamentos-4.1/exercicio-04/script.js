/*
Utilize if/else para fazer um programa que, dado um valor recebido
como parâmetro, retorne “positive” se esse valor for positivo,
“negative” se for negativo, e caso não seja nem positivo e nem
negativo retorne “zero”.
*/

let a = 10;

if (a < 0) {
    console.log('esse número é negativo')
}
else if (a > 0){
    console.log('esse número é positivo')
}
else {
    console.log('esse número é zero')
}